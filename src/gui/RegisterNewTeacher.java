    private int genarateUniqNumber() {
        int DIGIT_COUNT = 6;
        int MAX_NUMBER = 999999;
        int MIN_NUMBER = 100000;
        Set<Integer> generatedNumbers = new HashSet<>();
        Random random = new Random();

        int number;
        do {
            number = random.nextInt((MAX_NUMBER - MIN_NUMBER) + 1) + MIN_NUMBER;
        } while (generatedNumbers.contains(number));
        generatedNumbers.add(number);

        System.out.println(number);
        return number;
    }

    public String generateUniqueTeacherID() {
        String teacherID;
        boolean isUnique;
        do {
            teacherID = "tea_" + genarateUniqNumber();
            String query = "SELECT `teacher`.`Tno` FROM `teacher` WHERE `teacher`.`Tno` = ?";
            try {
                ResultSet result = MySQL.executeSelect(query, teacherID);
                isUnique = !result.next();
            } catch (SQLException e) {
                e.printStackTrace();
                isUnique = false;
            }
        } while (!isUnique);
        return teacherID;
    }

